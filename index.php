    function pilihandigit(digit) { 
        var digits = "";
        if (digit ==1) {
		  digits = '0123456789'; 
        }else if (digit ==2) {
		  digits = 'abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';  
        }else if (digit ==3) {
		  digits = `0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ`; 
        }else if (digit ==4) {
		  digits = `0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ~!@#$%^&*()_+[]{}:;<>?,.`; 
        }else{
            digits = digit;
        }
        return digits;
    }
    function randomBetween(awal, akhir) {
        var random = 0;
        random = Math.floor(Math.random() * akhir) + awal ; 
        return random;
    }
    function generate(awal, akhir, digit) { 
		  var gen = ''; 
